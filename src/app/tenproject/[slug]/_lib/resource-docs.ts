export function openResourceDoc(title: string) {
  if (title.includes('Week 4') && title.includes('school')) {
    openDocWindow(shell('Week 4 session plan — school', sessionPlanBody('school')))
    return
  }
  if (title.includes('Week 4') && title.includes('family')) {
    openDocWindow(shell('Week 4 session plan — family', sessionPlanBody('family')))
    return
  }
  if (title.includes('Session cards')) {
    // All 10 weeks — reuse the scheme of work
    const d = GENERIC_DOCS['Scheme of work — all 10 weeks']
    openDocWindow(shell(d.title, `${brandHeader('SESSION CARDS · ALL 10 WEEKS')}<h1>${esc(d.title)}</h1><div class="sub">${esc(d.sub)}</div>${d.body}<div class="foot">Ten Project Ltd · Generated by the Ten Project Portal</div>`))
    return
  }
  if (title.includes('TENOR handbook')) {
    openDocWindow(shell('TENOR handbook', `${brandHeader('TENOR HANDBOOK')}<h1>The TENOR handbook</h1><div class="sub">Everything about running a family session — the short version</div>${ul(['Arrive 20 minutes early — setup, court sweep, QR sign on the gate', 'Families scan themselves in; tap in anyone without a phone', 'Follow the week’s session card — warm-up, drills, games, together time', 'Minimum two inducted TENORs for a session to run', 'Any safeguarding concern: call the HQ lead, never investigate yourself', 'Register auto-submits at 3pm — check the live count before you leave', 'Pack down: kit counted back into the store, damage reported in the app'])}<div class="foot">Ten Project Ltd · Generated by the Ten Project Portal</div>`))
    return
  }
  if (title.includes('Safeguarding basics')) {
    const d = GENERIC_DOCS['Safeguarding: quick reference']
    openDocWindow(shell(d.title, `${brandHeader('SAFEGUARDING · VOLUNTEERS')}<h1>${esc(d.title)}</h1><div class="sub">${esc(d.sub)}</div>${d.body}<div class="foot">Ten Project Ltd · Generated by the Ten Project Portal</div>`))
    return
  }
  const d = GENERIC_DOCS[title]
  const doc = d ?? { title, sub: 'Resource', body: '<p>Full content lands here from the portal resource library.</p>' }
  openDocWindow(shell(doc.title, `${brandHeader('RESOURCE')}<h1>${esc(doc.title)}</h1><div class="sub">${esc(doc.sub)}</div>${doc.body}<div class="foot">Ten Project Ltd · Generated by the Ten Project Portal</div>`))
}

export function openSessionCardDoc() {
  openDocWindow(shell('Session card — Week 4', sessionPlanBody('family')))
}
