const table = (head: [string, string], rows: [string, string][]) =>
  `<table><tr><th>${esc(head[0])}</th><th>${esc(head[1])}</th></tr>${rows.map(r => `<tr><td><b>${esc(r[0])}</b></td><td>${esc(r[1])}</td></tr>`).join('')}</table>`

const ul = (items: string[]) => `<ul>${items.map(i => `<li style="margin-bottom:6px">${esc(i)}</li>`).join('')}</ul>`

function sessionPlanBody(variant: 'school' | 'family') {
  const p2 = WEEK4_SESSION_PLAN
  return `
    ${brandHeader(`SESSION PLAN · WEEK ${p2.week} · ${variant.toUpperCase()}`)}
    <h1>Week ${p2.week} — ${p2.skill} (${variant === 'school' ? 'in-school session' : 'weekend family session'})</h1>
    <div class="sub">${esc(p2.duration)} · ${variant === 'school' ? 'coach-led, teacher assisting' : 'TENOR-led with coach support — parent + child pairs'}</div>
    ${table(['Time', 'Block'], p2.blocks.map(b => [`${b.time} (${b.mins}m)`, `${b.title} — ${b.detail}`] as [string, string]))}
    <h2>COACHING POINTS</h2>
    ${ul(p2.coachingPoints)}
    <h2>EQUIPMENT</h2>
    ${ul(p2.equipment)}
    <h2>SAFETY & SAFEGUARDING</h2>
    ${ul(p2.safety)}
    <h2>THIS WEEK’S VIDEOS</h2>
    ${ul(p2.videos.map(v => `${v.title} (${v.length})`))}
    <div class="foot">Ten Project Ltd · Generated by the Ten Project Portal</div>
  `
}

