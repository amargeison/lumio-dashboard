export function openFunderDoc(kind: FunderDocKind) {
  const html =
    kind === 'term-report' ? termReport()
    : kind === 'funder-pack' ? funderPack()
    : kind === 'governor-pack' ? governorPack()
    : kind === 'newsletter-snippet' ? newsletterSnippet()
    : bidNarrative()
  const w = window.open('', '_blank')
  if (!w) return
  w.document.open()
  w.document.write(html)
  w.document.close()
}

export function openDocWindow(html: string) {
  const w = window.open('', '_blank')
  if (!w) return
  w.document.open()
  w.document.write(html)
  w.document.close()
}
