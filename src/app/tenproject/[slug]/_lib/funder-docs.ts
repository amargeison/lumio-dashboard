export function brandHeader(docLabel: string) {
  return `<div class="brand">
    <img src="/tenproject_logo.png" alt="Ten Project">
    <div class="doc">${esc(docLabel)}<br>${new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })}</div>
  </div>`
}

const tiles = (items: { label: string; value: string | number }[]) =>
  `<div class="tiles">${items.map(t => `<div class="tile"><b>${esc(String(t.value))}</b><span>${esc(t.label)}</span></div>`).join('')}</div>`

